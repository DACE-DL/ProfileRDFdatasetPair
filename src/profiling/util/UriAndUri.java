package profiling.util;

public class UriAndUri {
	
	private String uri1;
	private String uri2;
	
	public UriAndUri(String uri1, String uri2) {
		this.uri1 = uri1;
		this.uri2 = uri2;
	}
	public String getUri1() {
		return uri1;
	}
	public void setUri1(String uri1) {
		this.uri1 = uri1;
	}
	public String getUri2() {
		return uri2;
	}
	public void setUri2(String uri2) {
		this.uri2 = uri2;
	}
	
}
